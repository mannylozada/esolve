class StudentsController < ApplicationController
  # GET /students
  # GET /students.json
  def index
    @students = params[:grade_id].blank? ? Student.all : Student.find_all_by_grade_id(params[:grade_id])
    @students = params[:esol_group_id].blank? ? @students : Student.find_all_by_esol_group_id(params[:esol_group_id])

    respond_to do |format|
      format.html # index.html.erb
      format.xml { render xml: @students }
      format.json { render json: @students }
    end
  end

  # GET /students/1
  # GET /students/1.json
  def show
    @student = Student.find(params[:id])
    @grades = Grade.all
    @languages = Language.all
    @esol_groups = EsolGroup.all

    respond_to do |format|
      format.html # show.html.erb
      format.xml { render xml: @student }
      format.json { render json: @student }
    end
  end

  # GET /students/new
  # GET /students/new.json
  def new
    @student = Student.new
    @grades = Grade.all
    @languages = Language.all

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @student }
    end
  end

  # GET /students/1/edit
  def edit
    @student = Student.find(params[:id])
    @grades = Grade.all
    @languages = Language.all
  end

  # POST /students
  # POST /students.json
  def create
    @student = Student.new(params[:student])

    respond_to do |format|
      if @student.save
        format.html { redirect_to @student, notice: 'Student was successfully created.' }
        format.json { render json: @student, status: :created, location: @student }
      else
        format.html { render action: "new" }
        format.json { render json: @student.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /students/1
  # PUT /students/1.json
  def update
    @student = Student.find(params[:id])

    respond_to do |format|
      if @student.update_attributes(params[:student])
        format.html { redirect_to @student, notice: 'Student was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @student.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /students/1
  # DELETE /students/1.json
  def destroy
    @student = Student.find(params[:id])
    @student.destroy

    respond_to do |format|
      format.html { redirect_to students_url }
      format.json { head :no_content }
    end
  end
end
